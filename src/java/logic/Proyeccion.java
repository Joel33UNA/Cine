package logic;

public class Proyeccion {
    int id;
    Sala sala;
    Pelicula pelicula;
    
    public Proyeccion(){
        this.id = 0;
        this.sala = new Sala();
        this.pelicula = new Pelicula();
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Sala getSala() {
        return sala;
    }

    public void setSala(Sala sala) {
        this.sala = sala;
    }

    public Pelicula getPelicula() {
        return pelicula;
    }

    public void setPelicula(Pelicula pelicula) {
        this.pelicula = pelicula;
    }
}
